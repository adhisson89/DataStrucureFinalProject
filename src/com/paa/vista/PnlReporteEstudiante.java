    public void llenarTabla(){
        // limpiar la tabla
        try {
            tabla.setRowCount(0);
            for (Estudiante e : FrmMenuPrincipal.estudiantes) {
                tabla.addRow(new Object[]{e.getNombre(), e.getApellido(), e.getCursos().size()});
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

